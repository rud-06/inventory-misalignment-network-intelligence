# ===============================
# 1️⃣2️⃣-B: SUPPLY CHAIN NERVOUSNESS (BULLWHIP INDEX)
# ===============================

# 1. Calculate Daily Closing Stock (Approximation from hours)
df['daily_closing_stock'] = df['hours_stock_status'].apply(lambda x: len(x) - sum(x))

# 2. Rolling Coefficient of Variation (CV) for Sales (7-day)
# CV = Sigma / Mu
df['sales_rolling_std'] = df.groupby(['store_id', 'product_id'])['calculated_daily_units'].transform(lambda x: x.rolling(7).std())
df['sales_rolling_mean'] = df.groupby(['store_id', 'product_id'])['calculated_daily_units'].transform(lambda x: x.rolling(7).mean())
df['sales_cv'] = df['sales_rolling_std'] / (df['sales_rolling_mean'] + 1e-6)

# 3. Rolling Coefficient of Variation (CV) for Inventory (7-day)
df['stock_rolling_std'] = df.groupby(['store_id', 'product_id'])['daily_closing_stock'].transform(lambda x: x.rolling(7).std())
df['stock_rolling_mean'] = df.groupby(['store_id', 'product_id'])['daily_closing_stock'].transform(lambda x: x.rolling(7).mean())
df['stock_cv'] = df['stock_rolling_std'] / (df['stock_rolling_mean'] + 1e-6)

# 4. Nervousness Index (Inventory Volatility / Demand Volatility)
# High Value (>1.5) = Panic Ordering / Erratic Supply
# Low Value (<0.5) = Passive / Neglect
df['nervousness_index'] = df['stock_cv'] / (df['sales_cv'] + 1e-6)

# 5. Classify the Behavior
df['manager_behavior'] = 'Synchronized'
df.loc[df['nervousness_index'] > 1.5, 'manager_behavior'] = 'Panic/Over-Reactive'
df.loc[df['nervousness_index'] < 0.5, 'manager_behavior'] = 'Passive/Neglect'

print("\nManager Behavior Distribution:")
print(df['manager_behavior'].value_counts())

