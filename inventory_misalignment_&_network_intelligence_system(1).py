# 1. Define "Surplus Sales" (Sales significantly above expected)
df['surplus_sales'] = df['calculated_daily_units'] - df['expected_demand_7d']
df['is_surplus'] = (df['surplus_sales'] > 0).astype(int)

# 2. Aggregating at Store-Category-Date Level
cat_group = df.groupby(['store_id', 'second_category_id', 'dt']).agg(
    total_stockout_loss=('revenue_at_risk', 'sum'),
    total_surplus_revenue=('sale_amount', lambda x: x[df.loc[x.index, 'is_surplus'] == 1].sum()),
    stockout_count=('revenue_at_risk', lambda x: (x > 0).sum())
).reset_index()

# 3. Calculate "Captured Revenue" (We assume up to 50% could be substitution)
cat_group['likely_substitution_val'] = cat_group[['total_stockout_loss', 'total_surplus_revenue']].min(axis=1) * 0.5

# 4. Merge back to main DF to adjust Revenue at Risk
df = df.merge(cat_group[['store_id', 'second_category_id', 'dt', 'likely_substitution_val']],
              on=['store_id', 'second_category_id', 'dt'],
              how='left')

# 5. Distribute substitution credit safely

df['net_revenue_at_risk'] = df['revenue_at_risk'].copy()
mask_stockout = df['revenue_at_risk'] > 0

stockout_counts = (
    df[mask_stockout]
    .groupby(['store_id','second_category_id','dt'])
    .size()
    .reset_index(name='stockout_group_count')
)

df = df.merge(
    stockout_counts,
    on=['store_id','second_category_id','dt'],
    how='left'
)

df['stockout_group_count'] = df['stockout_group_count'].fillna(0)

df.loc[mask_stockout, 'net_revenue_at_risk'] -= (
    df.loc[mask_stockout, 'likely_substitution_val'] /
    df.loc[mask_stockout, 'stockout_group_count']
)

df['net_revenue_at_risk'] = df['net_revenue_at_risk'].clip(lower=0)

print(f"Gross Revenue at Risk: {df['revenue_at_risk'].sum():,.2f}")
