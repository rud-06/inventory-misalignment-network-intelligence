# 1. Define Destinations (Stores Needing Stock)
# Logic: We filter for stores losing > $3.00 per day on this item.
# This represents a "Significant Operational Failure" worth fixing.
destinations = df[
    (df['net_revenue_at_risk'] > 3.0) &       
    (df['dt'] == df['dt'].max())            
][['store_id', 'product_id', 'net_revenue_at_risk', 'dt', 'city_id']].copy() 

# 2. Define Sources (Stores with Excess Stock)
# Logic: Service Rate = 1.0 (Perfectly in stock) AND
# Velocity Flag is NOT 'accelerating' (They don't need it urgently)
sources = df[
    (df['hourly_service_rate'] == 1.0) &
    (~df['velocity_flag'].isin(['accelerating'])) & 
    (df['dt'] == df['dt'].max())
][['store_id', 'product_id', 'dt', 'city_id']].copy()

# 3. Rename columns for merging
destinations = destinations.rename(columns={'store_id': 'to_store', 'net_revenue_at_risk': 'potential_gain'})
sources = sources.rename(columns={'store_id': 'from_store'})

# 4. Create Network Graph (Strict Match on Product & City)
# We strictly match on 'city_id' to ensure transport costs are negligible.
transfers = pd.merge(destinations, sources, on=['product_id', 'city_id', 'dt'])

# 5. Filter Self-Transfers
transfers = transfers[transfers['to_store'] != transfers['from_store']]

# 6. Calculate "Net Opportunity Value"
# Gain ($3+) - Cost ($0.50 local transport)
transfers['transfer_net_value'] = transfers['potential_gain'] - 0.50
