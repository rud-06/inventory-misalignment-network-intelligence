# ===============================

# Total hours
df["total_hours"] = df["hours_stock_status"].apply(len)

# Stockout hours (1 = stockout)
df["stockout_hours"] = df["hours_stock_status"].apply(lambda x: np.sum(x))

# In-stock hours
df["in_stock_hours"] = df["total_hours"] - df["stockout_hours"]

# Basic hourly service rate
df["hourly_service_rate"] = df["in_stock_hours"] / df["total_hours"]

# ===============================
# 5️⃣ DEMAND STRUCTURE METRICS
# ===============================

# Daily total sales (sanity check)
df["calculated_daily_units"] = df["hours_sale"].apply(lambda x: np.sum(x))

# Peak demand
df["peak_hour_sales"] = df["hours_sale"].apply(lambda x: np.max(x))

# Average hourly demand
df["avg_hourly_sales"] = df["hours_sale"].apply(lambda x: np.mean(x))

# Demand concentration ratio
df["sales_variation"] = df["peak_hour_sales"] / (df["avg_hourly_sales"] + 1e-6)

# ===============================
# 6️⃣ DEMAND-WEIGHTED SERVICE LEVEL (MEMORY SAFE)
# ===============================

dws = []

for sales, stock in zip(df["hours_sale"], df["hours_stock_status"]):

    total_sales = sales.sum()

    if total_sales > 0:
        availability = 1 - stock   # 1 = available, since stock column is stockout flag
        dws_value = (sales * availability).sum() / total_sales
        dws.append(dws_value)
    else:
        dws.append(np.nan)

df["demand_weighted_service"] = dws

# ===============================
# 7️⃣ SUMMARY CHECK
